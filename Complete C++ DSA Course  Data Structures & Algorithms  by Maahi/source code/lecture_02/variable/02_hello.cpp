#include <iostream>
using namespace std;

int main()
{   // primitive data types
    int age = 20;
    char name[] = "maahi";
    float height = 5.5f;
    cout << "-----------" << endl;
    cout << "hello world!" << endl;
    cout << "my name is " << name << endl;
    cout << "size of name is " << sizeof(name) << endl;
    cout << "my age is " << age << endl;
    cout << "size of age is " << sizeof(age) << endl;
    cout << "my height is " << height << endl;
    cout << "size of height is " << sizeof(height) << endl;

    cout << "-----------" << endl;
    cout << "thank you!" << endl;
    cout << "maahi.." << endl;
    return 0;
}