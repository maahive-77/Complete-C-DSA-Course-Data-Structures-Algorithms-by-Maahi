#include <iostream>
using namespace std;

int main() {
    // arithmetics operator 
    // add , subtract , multiply , divide , modulus;
    int a = 10, b = 5;
    int add , subtract, multiply,divide,remainder;

    add = a + b ;
    subtract = a - b;
    multiply = a*b;
    divide = a/b;
    remainder = a%b;

    cout << "add = "<<add<<endl;
    cout << "subtract = "<<subtract<<endl;
    cout << "multiply = "<<multiply<<endl;
    cout << "divide = "<<divide<<endl;
    cout << "remainder = "<<remainder<<endl;

    cout << "-----------" << endl;
    cout << "thank you!" << endl;
    cout << "maahi.." << endl;
    return 0;
}